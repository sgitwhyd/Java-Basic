package com.dicoding.javafundamental.basic.operator;

public class OperatorAritmatika {
    public static void main(String[] arg){
        System.out.println("Operasi Penjumlahan");
        int hasilPenjumlahan = 5 + 10;
        System.out.println("Hasil penjumlahan 5 + 10 adalah " + hasilPenjumlahan );

        System.out.println("Operasi Pengurangan");
        int hasilPengurangan = 5 - 10;
        System.out.println("Hasil pengurangan 5 - 10 adalah " + hasilPengurangan);

        System.out.println("Operasi Pembagian");
        int hasilPembagian = 50 / 10;
        System.out.println("Hasil pembagian 50 / 10 adalah " + hasilPembagian);

        System.out.println("Operasi hasi sisa bagi");
        int hasilSisaBagi = 50 % 3;
        System.out.println("Hasil sisa pembagian 50 % 3 adalah " + hasilSisaBagi);


    }
}